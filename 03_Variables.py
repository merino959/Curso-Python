# Asignar un valor a una variable:
# variable = expresion
# La asignacion consiste en evaluar la expresion a la derecha del simbolo =, y guardar
# el valor resultante en la variable indicada a la izquierda del simbolo =.
pi = 3.1415
r = 1.2983
perimetro = 2 * pi * r
area = pi * r**2
print(perimetro)
print(area)

# El valor de x es 3. Despues se evalua la expresion x + 1, es decir 3 + 1
# el resultado que es 4 se guarda en la variable x, es decir el valor de x es ahora 4.
x = 3
x = x + 1
print(x)

# Asignaciones con Operador
a = 3
b = 2
a *= 4 + b
print(a)
