# Calcula el valor absoluto de un número
a = abs(-3)  # a = 3
a = abs(3)  # a = 3

# Conversión a flotante
a = float(3)  # a = 3.0
a = float("3.2")  # a = 3.2

# Conversión a entero
a = int(2.1)  # a = 2
a = int(-2.9)  # a = -2
a = int("2")  # a = 2

# Conversión a cadena
a = str(2.1)  # a = "2.1"

# Redondeo
a = round(2.1)  # a = 2
a = round(2.9)  # a = 3
a = round(-2.9)  # a = -3
a = round(2.1451, 2)  # a = 2.15
a = round(2.1451, 3)  # a = 2.145
a = round(2.1451, 0)  # a = 2.0
